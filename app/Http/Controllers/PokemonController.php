     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $errCode = 0;

        if(empty($request->input('name')))
        {
            // Return error
            $errCode = 1;
            return response()->json(['errCode' => $errCode]);
        }

        $poke = new Pokemon;
        $poke->name = $request->input('name');

        if(!$poke->save())
        {
            // Return error
            $errCode = 2;
            return response()->json(['errCode' => $errCode]);
        }

        return response()->json(['errCode' => $errCode]);
    }

